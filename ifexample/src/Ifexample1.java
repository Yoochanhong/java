public class Ifexample1 {
    public static void main(String[] args) {
        int age = 7;
        if(age >=8){ // 만약 age가 8 이상이라면
            System.out.println("학교에 다닙니다."); //출력
        }
        else{ //아니면
            System.out.println("학교에 다니지 않습니다."); //이거 출력
        }
    }
}

