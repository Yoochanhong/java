public class CharacterEx1 {
    public static void main(String[] args) {
        char ch1 = 'A';
        System.out.println(ch1); //A출력
        System.out.println((int)ch1); // A의 아스키코드값 65출력

        char ch2 = 66;
        System.out.println(ch2); //66의 아스키코드 'B' 출력

        int ch3 = 67;
        System.out.println(ch3); // 67 출력
        System.out.println((char)ch3); // 67의 아스키코드 'C' 출력
    }
}
